class AddOp {
    public static void main(String[] args) {
        System.out.println(125 + 463);
        System.out.println(2.0 - 6.3);
        int age = 65;
        System.out.println(age);
        System.out.println(age--);
        System.out.println(age++);
        System.out.println(--age);
        System.out.println(++age);
        System.out.println("A" + "B");
    }
}